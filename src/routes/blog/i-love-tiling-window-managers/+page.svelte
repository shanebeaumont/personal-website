        <h1>I Love Tiling Window&nbsp;Managers</h1>
    </div>

    <p>Switching to Arch&nbsp;Linux, I was excited by the possibilities of configuring my system. However, I was initially skeptical about <a href="https://en.wikipedia.org/wiki/Tiling_window_manager">Tiling Window Managers</a> (TWMs). It seemed cumbersome at first; I didn't understand how they were navigated. But after some exploration, I discovered their true potential. Here's why I now love using TWMs.</p>

    <h2>What is a Window&nbsp;Manager?</h2>

    <p>Before diving into TWMs, it's important to understand what <a href="https://en.wikipedia.org/wiki/Window_manager">window managers</a> are and the <strong>purpose</strong> they serve. Simply put, a window manager is the software that <strong>manages</strong> the <strong>position</strong>, <strong>size</strong>, and <strong>appearance</strong> of <strong>windows</strong> on your screen.</p>

    <h3>Stacking Window&nbsp;Managers</h3>

    <p>Traditional <a href="https://en.wikipedia.org/wiki/Desktop_environment">desktop environments</a>, such as those on <strong>Windows</strong> and <strong>macOS</strong>, use <a href="https://en.wikipedia.org/wiki/Stacking_window_manager">stacking window managers</a>. This can result in windows being hidden beneath others, creating a <strong>cluttered</strong> and <strong>disorganised</strong> workspace.</p>

    <h3>Tiling Window&nbsp;Managers</h3>

    <p>TWMs manage windows by <strong>automatically</strong> dividing the screen into <strong>non&#8209;overlapping</strong> tiles. This fosters a more <strong>efficient</strong>, <strong>organised</strong>, and <strong>focused</strong> workspace for a <strong>streamlined</strong>, <strong>keyboard&#8209;navigable</strong> workflow.</p>

    <h3>Compositing Window&nbsp;Managers</h3>

    <p>If you decide to research window managers, you may encounter the term '<a href="https://en.wikipedia.org/wiki/Compositing_window_manager">compositing window managers</a>'. This is simply referring to how they <strong>render</strong> windows; they are still <strong>functionally</strong> either <strong>stacking</strong> or <strong>tiling</strong> window managers.</p>

    <h2>Types of TWMs</h2>

    <p>Different TWMs handle window arrangement in various ways, typically falling into two main categories: manual and dynamic. Manual TWMs require users to explicitly control the direction of new windows within the layout, while dynamic TWMs automatically arrange windows into the layout.</p>

    <p>There are various layouts for TWMs too, however I won't go into detail on those as they tend to be quite similar for most use cases, where you'll only have one or two windows per workspace.</p>

    <h2>Workflow</h2>

    <p>In TWMs, <strong>workspaces</strong> are far more <strong>flexible</strong> and <strong>tightly integrated</strong> compared to <strong>Virtual Desktops</strong> in Windows or <strong>Spaces</strong> in macOS, where you can only <strong>cycle</strong> through adjacent workspaces one at a time. In contrast, TWMs allow users to jump <strong>directly</strong> to any specific workspace with a <strong>single keybind</strong>, each containing its own set of tiled windows.</p>

    <p>The typical arrangement is to have a <strong>separate workspace</strong> for <strong>each open window</strong>, similar to how a browser uses a tab for each open website. When you want to view <strong>multiple windows</strong> at once, you open them within the <strong>same workspace</strong>, and the window manager automatically arranges them for you.</p>

    <p>Proficiency with TWMs greatly <strong>enhances workflow</strong> by minimising window interactions. Their <strong>keyboard-centric</strong> design enables <strong>fast</strong> window management through simple keybinds, and when paired with keyboard-centric applications, you can work efficiently <strong>without a mouse</strong>.</p>

    <h3>Scratchpads</h3>

    <p>My favourite TWM feature is the scratchpad. It holds windows <strong>temporarily</strong> accessible with <strong>keybindings</strong>, typically for floating windows like terminals, note-taking, and instant messengers, though any window can be assigned to it.</p>

    <p>The scratchpad <strong>streamlines workflow</strong> by providing quick access to essential tools without cluttering your workspaces and eliminating the need to search for windows.</p>

    <h2>Customisation</h2>

    <h3>Keybindings</h3>

    <p>TWMs allow you to create custom keybindings for <strong>quickly</strong> launching specific applications and executing scripts to <strong>automate</strong> window management and system functions.</p>

    <p>For example, I have a keybinding on my laptop that executes a script to adjust brightness along an exponential curve, allowing for finer control at lower levels.</p>

    <h3>Window Rules</h3>

    <p>Window rules configure how <strong>specific</strong> windows behave, controlling attributes such as size, position, floating behaviour, and workspace assignment, further <strong>automating</strong> window management.</p>

    <h3>Aesthetics</h3>

    <p>TWMs are admired for their aesthetic appeal and extensive personalisation options. Users can customise <strong>every aspect</strong> of the interface, including colours, fonts, window borders, and spacing, allowing for a <strong>unique</strong> and <strong>visually pleasing</strong> workspace.</p>

    <h2>Conclusion</h2>

    <p>Tiling window managers offer an efficient, customizable, and unique approach to interacting with your computer. With daily tasks managed effectively through the tiling layout, scratchpad, keybindings, and window rules, you can create a tailored environment that enhances your workflow, productivity, and focus. While there may be a learning curve and some initial setup time, I highly encourage you to give them a try and experience the benefits for yourself. The effort will be well worth it!</p>
